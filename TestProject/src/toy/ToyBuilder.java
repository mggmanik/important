package toy;

public abstract class ToyBuilder {

    private int price;
    private String color;

    public abstract void immovable();

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
